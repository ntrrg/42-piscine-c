int	ft_sqrt(int nb)
{
	int i;
	int sqr;

	if (nb < 0)
		return (0);
	i = 0;
	while (i < nb)
	{
		sqr = i * i;
		if (i * i < 0)
			return (0);
		if (i * i == nb)
			return (i);
		i++;
	}
	return (0);
}
